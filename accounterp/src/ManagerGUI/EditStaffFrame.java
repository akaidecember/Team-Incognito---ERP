    public String isValidDate(String str) {
        String result = "";
        try {
            int x = Integer.parseInt(str);
            if (x > 31) {
                result = "Invalid Date \n Please Re enter ";
            }
        } catch (Exception ex) {
            result = "Date is not a Number";
            System.out.println("Exception ex " + ex);
        }
        return result;
    }

