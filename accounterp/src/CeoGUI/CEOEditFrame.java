    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String opass=jPasswordField3.getText();
        String npass = jPasswordField1.getText();
        String cpass = jPasswordField2.getText();
        String uname = LoginFrame.uname;
      
        

        if (opass.isEmpty()) 
        {
            JOptionPane.showMessageDialog(null, "Old Password Cannot be Empty");

        } 
        else if (npass.isEmpty()) 
        {
            JOptionPane.showMessageDialog(null, "Password Cannot be Empty");

        } 
        else if (cpass.isEmpty()) 
        {
            JOptionPane.showMessageDialog(null, " Confirm Password Cannot be Empty");
        } 
        else if (!npass.equals(cpass)) 
        {
            JOptionPane.showMessageDialog(null, " Password and Confirm Password Doesn't Match Please Try Again");
        } 
        else 
        {
            
            CEOProfileDBOperation md = new CEOProfileDBOperation();
            
            if(md.checkPassword(opass))
           {
              md.updatePassword(uname, cpass, LoginFrame.uname, LoginFrame.date);
              JOptionPane.showMessageDialog(null, "CEO Password Updated Successfully");
               jPasswordField3.setText("");
               jPasswordField1.setText("");
               jPasswordField2.setText("");
               this.dispose();
                Dimension d = Toolkit.getDefaultToolkit().getScreenSize();
            LoginFrame l = new LoginFrame();
            l.setSize(d);
            l.revalidate();
            l.setVisible(true);
              
           }
           
            else 
            {
                JOptionPane.showMessageDialog(null, "Old Password Not Correct");
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

