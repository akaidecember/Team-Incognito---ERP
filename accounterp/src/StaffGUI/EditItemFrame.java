    private void jButton1KeyPressed(java.awt.event.KeyEvent evt) {// GEN-FIRST:event_jButton1KeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String type = "";
            String itemname = jTextField1.getText();
            String idescription = jTextArea1.getText();
            String unitx = (String) jComboBox2.getSelectedItem();
            String unity = (String) jComboBox3.getSelectedItem();
            String unit1 = 1 + ":" + unitx;
            String unit2 = jTextField6.getText() + ":" + unity;
            String hsnsac = jTextField4.getText();
            String gstrate = (String) jComboBox1.getSelectedItem();

            if (jRadioButton1.isSelected()) {
                type = jRadioButton1.getText();
            } else if (jRadioButton2.isSelected()) {
                type = jRadioButton2.getText();

            }

            if (itemname.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Item Name Cannot be Empty");

            } else if (jRadioButton1.isSelected() && unitx.equals("None")) {
                JOptionPane.showMessageDialog(null, "Please Select Unit1");
            } else if (hsnsac.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please Select HSN/SAC Code");
            } else if (gstrate.equals("Select")) {
                JOptionPane.showMessageDialog(null, "Please Select GST Rate");
            } else {
                ManageItemDBOperation md = new ManageItemDBOperation();
                if (md.updateIteminfo(type, sname, idescription, unit1, unit2, hsnsac, gstrate)) {
                    JOptionPane.showMessageDialog(null, "Item Information Updated Successfully");
                    jRadioButton1.setSelected(false);
                    jRadioButton2.setSelected(false);
                    jTextField1.setText("");
                    jTextArea1.setText("");
                    jComboBox2.setSelectedItem("None");
                    jComboBox3.setSelectedItem("None");
                    jLabel9.setText("Unit1");
                    jLabel10.setText("Unit2");
                    jTextField5.setText("");
                    jTextField4.setText("");
                    jTextField6.setText("");
                    jComboBox1.setSelectedItem("Select");

                } else {
                    JOptionPane.showMessageDialog(null, "Oops There is Some Error Please Try Again");
                }

                // System.out.println("Type: "+type);
                // System.out.println("Item Name: "+itemname);
                // System.out.println("Product Description: "+idescription);
                // System.out.println("Unit1: "+unit1);
                // System.out.println("Unit2: "+unit2);
                // System.out.println("HSN/SAC: "+hsnsac);
                // System.out.println("GST Rate: "+gstrate);
            }
        }
    }// GEN-LAST:event_jButton1KeyPressed

